
let num = 10.9
console.log(Math.round(num))// rounds num if is .5 or above in decimal notation.
console.log(Math.floor(num)); // returns whole number as it is(no round off/ignores decimals)
console.log(Math.ceil(num));// round num if has .01 or .1 and above to nearest whole
console.log(Math.sign(num))// returns +ve or -ve according to the num